def main() -> None:
    content = SLIDES.read_text(encoding="utf-8")

    # Split keeping the separator lines as captured groups so we can rebuild
    parts = re.split(r"(^---$)", content, flags=re.MULTILINE)

    # Layout: parts[0]='' (before first ---), [1]='---', [2]=file-fm-content,
    # [3]='---', [4]=slide1-content, [5]='---', [6]=slide2-frontmatter,
    # [7]='---', [8]=slide2-content, ...
    # Slide N content is at parts[4N].
    for slide_idx in range(len(NOTES)):
        part_idx = 4 * (slide_idx + 1)
        note_block = f"\n\n<!--\n{NOTES[slide_idx].strip()}\n-->\n"
        parts[part_idx] = parts[part_idx].rstrip() + note_block

    SLIDES.write_text("".join(parts), encoding="utf-8")
    print(f"injected {len(NOTES)} notes into {SLIDES.name}")


if __name__ == "__main__":
    main()
